package mx.unam.ciencias.edd.proyecto3.estructuras_svg;
/**
* Enumeración para identificar el tipo de estructura que el usuario pretende desplegar.
*/
public enum EstructuraDatos{
  /* Estructura de datos Lista*/
  LISTA,
  /* Estructura de datos Cola*/
  COLA,
  /* Estructura de datos Pila*/
  PILA,
  /* Estructura de datos Arbol Binario Ordenado*/
  ARBOLORDENADO,
  /* Estructura de datos Arbol Binario Rojinegro */
  ARBOLROJINEGRO,
  /* Estructura de datos Arbol Binario AVL*/
  ARBOLAVL,
  /* Estructura de datos Arbol Binario Completo*/
  ARBOLCOMPLETO,
  /* Estructura de datos Gráfica */
  GRAFICA,
  /* Estructura de datos Montículo Mínimo */
  MONTICULOMINIMO,
  /* En caso de no haber ingresado una estructura de datos válida  */
  NINGUNO
}
